<p>왜 케이스 스터디가 이야기처럼 읽히어야 하는 거죠? 위에 있는 틀에 박힌 기사와 용의 이야기는 지루해 보여요. 행복한 결말 때문이 아니라 기사가 진짜로 어려움에 직면하지 않고 용을 쓰러뜨리기 위해 무엇인가를 새롭게 배우거나 적응할 필요가 없기 때문입니다. 그의 능력은 만능하며, 무엇을 해도 완벽하게 해내죠. 그것은 사실상 이야기가 아니에요.</p>
<p>실제로, 용을 죽이는 기사는 끔찍한 위험에 직면해야 합니다. 용이 불을 뿜으면 그 방패가 너무 뜨거워져서 들고 다닐 수 없게 될 것이라 생각합니다 (이상적인 디자인 프로세스를 포기하세요). 그는 용에 칼을 찌르려고 첫 번째 시도 때 당장 성공하지 못할 거예요 (너무 일찍 Figma에서 아름다운 시각적 디자인을 만드느라 실망하지 마세요). 그는 후퇴해야 하며, 자신에 대한 의심에 마주칠 것이고, 용기를 내고, 배우고, 적응하고, 새로운 계획을 세우고, 다시 시도하며, 성공하기까지 반복해야 할 것입니다. 이런 세부 사항들이 기사를 흥미롭고 인간적으로 만드는 것이죠. 그를 이해할 수 있습니다.</p>
<p>디자인 매니저도 마찬가지입니다. 케이스 스터디를 통해 이야기를 전달하고, 일이 제대로 풀리지 않았던 점과 어떻게 문제를 극복했는지를 포함한다면, 디자인 매니저는 당신과 공감할 것입니다. 당신의 케이스 스터디를 읽는 데 5분이 걸리더라도, 그들은 당신의 입장에 서 있을 거예요. 그들에게 비슷한 문제가 있었던 모든 순간을 상기시키고, 당신과 디자이너로서의 발버둥을 감사히 여길 거에요.</p>
