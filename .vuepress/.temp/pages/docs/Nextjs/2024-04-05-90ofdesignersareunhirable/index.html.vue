<p>그녀는 자신의 사례 연구에서 모든 것을 설명하지 못했어요. 그녀는 하나의 잠재적 솔루션에서 다른 것으로 이동하면서 어떤 것이 작동할 때까지의 선형 이야기로 읽히도록 단순화했어요. 그녀는 자신의 약점 중 하나에 직면하고 더 강력한 디자이너가 되기 위해 어떻게 했는지 흥미로운 이야기를 전하지 못했어요.</p>
