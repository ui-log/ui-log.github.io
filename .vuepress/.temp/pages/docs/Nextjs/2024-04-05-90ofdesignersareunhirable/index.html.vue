<p>제목 아래에서 약속된 해결 방법에 대해 말씀드리겠어요. 만약 일반적인 사례 연구가 기사와 용에 관한 이야기였다면, 이것이 그런 내용일 거예요:</p>
<p>마을을 괴롭히는 용에 대해 알게 되었고, 그 마을 사람들이 용을 처치한 기사에게 큰 양의 황금을 지불할 의향이 있다는 사실을 알게 되었어요. 마을 사람들에게 동굴의 위치와 용을 처치하는 방법을 물었어요. 용의 동굴을 찾아 모래 위에 자갈을 이용한 계획을 세우고, 용을 찾아 나섰어요. 용이 화염을 내뿜었지만, 방패로 막아내고, 그 후에 검을 용에 찔러 넣어 처치했어요. 금을 수집하여, 해피한 날이었어요.</p>
<p>기사를 디자이너로, 마을 사람들을 사용자로, 자갈을 포스트잇 노트로, 용을 디자인 문제로, 방패를 디자인 과정으로, 검을 Figma로 대체하면 전형적인 일반적인 UX 사례 연구가 됩니다. 그러나 어떤 용도 약간의 검 찔러 넣기로 처치되지 않듯이, 아름다운 프로토타입만으로 해결되는 디자인 문제는 없다는 것을 기억해주세요.</p>
