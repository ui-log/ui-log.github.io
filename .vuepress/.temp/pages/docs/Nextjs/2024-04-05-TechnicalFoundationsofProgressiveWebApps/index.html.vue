<li>display: 전체 화면, 스탠드얼론, 브라우저 등 원하는 디스플레이 모드를 지정하여, 홈 화면에서 앱을 실행할 때의 앱 외관에 영향을 줍니다.</li>
<li>background_color 및 theme_color: 스플래시 화면의 배경색 및 주소 표시줄 및 작업 전환기의 테마 색상을 정의합니다.</li>
<li>orientation: 웹 애플리케이션의 기본 방향을 정의합니다. 이는 애플리케이션의 요구에 따라 세로, 가로 또는 자연스러운 방향일 수 있습니다.</li>
</ul>
<p>이러한 옵션들을 통해 사용자의 기기와 선호도에 잘 통합되며 맞춤형 외관 및 동작을 제공할 수 있습니다.</p>
<p>웹 앱 매니페스트의 목표 중 하나는 PWA가 네이티브 앱과 일관된 사용자 경험을 제공하도록 하는 것입니다. 이는 운영 체제의 작업 전환기, 알림 시스템 및 설정과의 통합을 포함합니다. 매니페스트에서 아이콘, 색상 및 기타 옵션을 지정함으로써 PWA는 높은 통합 수준을 달성하여 네이티브 애플리케이션과 거의 구별할 수 없는 수준의 외관을 제공할 수 있습니다.</p>
