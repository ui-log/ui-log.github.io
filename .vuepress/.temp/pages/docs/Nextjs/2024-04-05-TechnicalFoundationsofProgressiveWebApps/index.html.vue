<h2 id="푸시-알림" tabindex="-1"><a class="header-anchor" href="#푸시-알림" aria-hidden="true">#</a> 푸시 알림</h2>
<p>푸시 알림은 점진적 웹 앱(PWA)의 필수 기능으로, 개발자들이 적시에 관련성 있는 콘텐츠로 사용자와 소통할 수 있도록 합니다. 이러한 알림은 사용자의 디바이스로 전송되어 웹 앱이 브라우저에서 열려 있지 않을 때에도 중요한 기능 중 하나인 네이티브 애플리케이션과 유사하게 작동할 수 있습니다.</p>
