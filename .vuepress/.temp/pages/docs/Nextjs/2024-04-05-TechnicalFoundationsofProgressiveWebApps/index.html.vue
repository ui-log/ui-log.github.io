<h2 id="manifest-파일" tabindex="-1"><a class="header-anchor" href="#manifest-파일" aria-hidden="true">#</a> Manifest 파일</h2>
<p>웹 앱 매니페스트는 사용자의 모바일 기기나 데스크톱에 '설치'될 때 웹 애플리케이션에 대한 정보와 동작 방식을 브라우저에 알려주는 간단한 JSON 파일입니다. 매니페스트를 가지고 있으면 웹 애플리케이션을 Progressive Web App (PWA)로 만드는 데 중요한 부분이 됩니다. 사용자가 앱을 홈 화면에 추가하고 앱을 홈 화면에서 시작할 때의 모습을 향상시킬 수 있습니다.</p>
<p>매니페스트는 JSON 텍스트 파일에 애플리케이션에 관한 정보(이름, 작성자, 아이콘, 설명 등)를 제공합니다. 매니페스트의 목적은 웹 애플리케이션을 기기의 홈 화면에 설치하여 사용자에게 더 빠른 액세스와 더 풍부한 경험을 제공하는 것입니다. 매니페스트를 사용하면 앱이 어떻게 나타나고 시작되는지 제어할 수 있습니다. 홈 화면 아이콘, 앱 시작 시 로드할 페이지, 화면 방향 및 브라우저 크롬 표시 여부 등을 지정할 수 있습니다.</p>
