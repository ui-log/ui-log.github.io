<p>매니페스트 파일에서 정의할 수 있는 주요 요소는 다음과 같습니다:</p>
<ul>
<li>name 및 short_name: 애플리케이션의 이름 및 홈 화면에 표시될 수 있는 더 짧은 이름.</li>
<li>아이콘: 다양한 맥락과 화면 크기에 대해 애플리케이션 아이콘으로 사용될 수 있는 이미지 객체의 배열.</li>
