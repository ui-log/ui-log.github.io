<p>검정은 0%의 색 밝기를 가지고 있고, 흰색은 100%의 색 밝기를 가지고 있어요. 색 밝기의 큰 차이는 우리 눈을 더 열심히 일하게 만들어요. 흰색 대비 순수한 검정을 피하는 것이 가장 안전하며, 대신 어두운 회색을 선택하는 것이 좋아요.</p>
<p>우리 예시에서, 순수 검정이 여러 요소에 사용되었어요. 이를 어두운 회색으로 변경하면 가독성이 향상되요. 이전에 시각적 계층 구조를 살펴볼 때, 속성 설명 텍스트가 너무 두드러지다고 느꼈어요. 인터페이스 요소가 중요도 순서대로 표시되도록 하기 위해, 속성 설명 텍스트에 더 밝은 회색을 사용하여 두드러지는 정도를 줄였어요.</p>
