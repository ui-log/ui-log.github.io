<h1 id="_3-비슷한-모습의-요소는-유사하게-작동하도록-보장하십시오" tabindex="-1"><a class="header-anchor" href="#_3-비슷한-모습의-요소는-유사하게-작동하도록-보장하십시오" aria-hidden="true">#</a> 3. 비슷한 모습의 요소는 유사하게 작동하도록 보장하십시오</h1>
<p>요소들이 비슷하게 보인다면, 사람들은 그들이 유사한 방식으로 작동할 것으로 기대할 것입니다. 그러므로, 같은 기능을 가진 요소에 일관된 시각적 처리를 사용하도록 노력하십시오. 반대로, 다른 기능을 가진 요소가 다르게 보이도록 노력하십시오.</p>
<p>우리의 예시에서, 아이콘 컨테이너는 &quot;지금 예약하기&quot; 버튼과 유사한 시각적 스타일을 가지고 있습니다. 이로 인해 그들은 상호작용적으로 보이지만, 실제로 그렇지 않습니다. 아이콘에서 파란색과 버튼 스타일을 제거하여 상호작용 요소로 오해되는 것을 피할 수 있습니다.</p>
