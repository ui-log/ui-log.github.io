<h1 id="_14-순수-검은색-텍스트-피하기" tabindex="-1"><a class="header-anchor" href="#_14-순수-검은색-텍스트-피하기" aria-hidden="true">#</a> 14. 순수 검은색 텍스트 피하기</h1>
<p>UI 디자인에서 순수 검정색을 피하는 것이 일반적으로 안전합니다. 순수 검은색은 흰색과 매우 높은 대비를 가지고 있기 때문에 텍스트를 읽을 때 눈에 스트레인을 유발하고 피로를 유발할 수 있습니다.</p>
