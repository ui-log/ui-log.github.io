<h1 id="_4-명확한-시각적-계층-구조-생성하기" tabindex="-1"><a class="header-anchor" href="#_4-명확한-시각적-계층-구조-생성하기" aria-hidden="true">#</a> 4. 명확한 시각적 계층 구조 생성하기</h1>
<p>인터페이스에 있는 모든 정보가 동일한 중요도를 갖지는 않습니다. 더 중요한 요소들을 더 두드러지게 만들어 중요도 순으로 정보를 제시하도록 노력해보세요.</p>
<p>명확한 중요도 순서 또는 시각적 계층 구조는 사람들이 정보를 빨리 스캔하고 관심 있는 영역에 집중할 수 있도록 도와줍니다. 이는 또한 순서감을 만들어 미적으로 개선함으로써 미적 감각을 향상시킵니다. 크기, 색상, 대조, 간격, 위치 및 깊이의 변화를 사용하여 명확한 시각적 계층 구조를 만들 수 있습니다.</p>
<p>여기에는 명확한 시각적 계층 구조가 없는 웹 사이트 히어로 배너의 예와 중요도 순서대로 요소가 명확하게 제시된 예가 이어집니다.</p>
