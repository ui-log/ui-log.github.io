<h2 id="_2-일관성-있게-유지" tabindex="-1"><a class="header-anchor" href="#_2-일관성-있게-유지" aria-hidden="true">#</a> 2. 일관성 있게 유지</h2>
<p>UI 디자인에서의 일관성은 유사한 요소가 비슷하게 보이고 작동한다는 것을 의미합니다. 이는 귀하의 제품 내에서도 그리고 다른 잘 알려진 제품과 비교했을 때도 동일해야 합니다. 이 예측 가능한 기능성은 사용성을 향상시키고 오류를 줄여줍니다. 사용자들이 작동 방식을 계속 학습할 필요가 없기 때문에 오류를 줄일 수 있습니다.</p>
