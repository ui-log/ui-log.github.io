<p>와이어프레임은 웹페이지의 요소 배치를 개요화한 레이아웃입니다. 자세한 디자인 및 개발에 앞서 다양한 레이아웃과 구조를 시각화하고 테스트하는 데 중요합니다. 프로토타입은 사용자 경험을 시뮬레이션하는 대화형 모델입니다. 실제 사용자와 함께 테스트하고 개선하여 최종 제품이 최대한 사용자 친화적이 되도록 합니다.</p>
<p>할 일:</p>
