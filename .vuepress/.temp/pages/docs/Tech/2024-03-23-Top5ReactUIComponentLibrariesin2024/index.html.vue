<h1 id="_4-chakra-ui" tabindex="-1"><a class="header-anchor" href="#_4-chakra-ui" aria-hidden="true">#</a> 4. Chakra UI</h1>
<p>Chakra UI는 접근성이 좋고 세련된 React 애플리케이션을 구축하기 위한 컴포넌트 라이브러리입니다. 버튼, 폼, 입력란, 메뉴 등과 같은 준비된 UI 컴포넌트 세트를 제공하여 프로젝트에 쉽게 통합할 수 있습니다.</p>
<h2 id="특징-3" tabindex="-1"><a class="header-anchor" href="#특징-3" aria-hidden="true">#</a> 특징:</h2>
<ul>
<li>사용하기 쉽고 이해하기 쉬운 컴포넌트: Chakra UI 컴포넌트는 직관적이고 명확하게 설계되어 초보자와 경험 많은 개발자 모두에게 이상적입니다.</li>
<li>모듈화 및 유연한 UI 구성: 각 컴포넌트는 독립적이며 독립적으로 사용할 수 있어 UI를 모듈화하고 유연하게 구성할 수 있습니다.</li>
<li>다양한 사용자 정의 옵션: 프롭스와 사용자 정의 테마를 사용하여 Chakra UI 컴포넌트의 거의 모든 측면을 수정하여 원하는 모양과 느낌을 만들어 낼 수 있습니다.</li>
<li>네이티브 다크 모드 지원: 대부분의 Chakra UI 컴포넌트는 기본적으로 다크 모드를 지원하여 앱에 세련되고 현대적인 다크 테마를 구현하기 쉽게 합니다.</li>
