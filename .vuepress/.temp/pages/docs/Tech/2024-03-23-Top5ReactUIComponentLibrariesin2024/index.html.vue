<h1 id="_5-ant-design" tabindex="-1"><a class="header-anchor" href="#_5-ant-design" aria-hidden="true">#</a> 5. Ant Design</h1>
<p>앤트 디자인은 주로 React를 사용하여 웹 애플리케이션을 구축하는 데 중점을 둔 견고하고 인기 있는 UI 디자인 언어 및 구성 요소 라이브러리입니다.</p>
<h2 id="특징-4" tabindex="-1"><a class="header-anchor" href="#특징-4" aria-hidden="true">#</a> 특징:</h2>
<ul>
<li>기업 중심: Ant Design는 B2B 사용자 인터페이스를 구축하는 데 탁월하며 효율성, 사용성 및 데이터 가시성을 우선시합니다.</li>
<li>매우 사용자 정의 가능: 구성 요소의 모든 측면을 사용자 정의할 수 있습니다. 크기, 색상, 레이아웃, 동작 등을 조정하여 특정 요구 사항과 브랜드 가이드라인과 일치시킬 수 있습니다.</li>
<li>광범위한 범위: 버튼, 폼, 테이블, 메뉴, 탐색 요소, 데이터 표시, 피드백 모듈 등 다양한 구성 요소가 사용 가능합니다.</li>
<li>모바일 친화적: 구성 요소는 다양한 화면 크기에 적응하여 다양한 기기에서 원활한 경험을 제공합니다.</li>
<li>국제화 지원: 인터페이스를 여러 언어로 번역하여 보다 넓은 사용자들에게 도달할 수 있습니다.</li>
</ul>
