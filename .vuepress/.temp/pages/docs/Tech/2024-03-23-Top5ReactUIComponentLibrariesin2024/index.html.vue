<h2 id="_2-headless-ui" tabindex="-1"><a class="header-anchor" href="#_2-headless-ui" aria-hidden="true">#</a> 2. Headless UI</h2>
<p><img src="@source/docs/Tech/2024-03-23-Top5ReactUIComponentLibrariesin2024/img/Top-5-React-UI-Component-Libraries-in-2024_2.png" alt="image"></p>
<p>Headless UI는 완전히 스타일이 없는, 완전히 접근 가능한 UI 구성 요소를 제공하는 React 라이브러리로, Tailwind CSS와 완벽하게 통합되도록 디자인되었습니다.</p>
<p>전통적인 CSS-in-JS 라이브러리와는 달리 Headless UI는 구성 요소의 행동과 접근성에 중점을 둬 시각적 스타일링은 완전히 사용자에게 맡깁니다.</p>
