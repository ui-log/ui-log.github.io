<h2 id="피타고라스-정리란-무엇인가요" tabindex="-1"><a class="header-anchor" href="#피타고라스-정리란-무엇인가요" aria-hidden="true">#</a> 피타고라스 정리란 무엇인가요?</h2>
<p>피타고라스 정리는 2500년 이상 된 오래된 이론으로 직각 삼각형의 변들 사이의 관골을 나타냅니다. 이 이론은 가장 긴 변(빗변 또는 위 다이어그램의 c)의 제곱이 다른 두 변의 제곱의 합과 같음을 명시합니다. 즉, a² + b² = c².</p>
<p>이 이론은 매우 어린 나이부터 교육 기관에서 교육되며, 3² + 4² = 5²와 5² + 12² = 13²와 같은 이론의 간단한 정수 해가 함께 제공됩니다. 이러한 정수 해를 종종 피타고라스의 삼중성으로 알려져 있습니다.</p>
<p>위의 삼각형을 참조하면 ⍺를 a의 길이와 c의 길이 사이의 각도로 두겠습니다. 그런 다음 삼각 함수 비율을 사용하면 sin⍺ = b/c 및 cos⍺ = a/c임을 알 수 있습니다. 따라서 sin²⍺ + cos²⍺ = (a² + b²)/c² = 1입니다. 따라서 인기있는 항등식 sin²⍺ + cos²⍺ = 1은 피타고라스 정리에서 유도되며 이 항등식을 사용하여 파생된 모든 항등식을 사용하는 피타고라스 정리의 증명은 원형 논리 때문에 무효입니다. 그렇지만, 삼각 함수 비율 자체는 피타고라스 정리와 독립적으로 증명될 수 있으므로, 피타고라스로부터 파생된 삼각 함수 항등식을 참조하지 않는 한, 이러한 비율을 증명에 사용하지 못할 이유가 없습니다.</p>
