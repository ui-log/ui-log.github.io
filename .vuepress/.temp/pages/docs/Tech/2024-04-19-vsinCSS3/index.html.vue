<p>언제나 콜론 &quot;:&quot; 또는 더블 콜론 &quot;::&quot;을 사용하는 엘리먼트를 스타일링할 때 계속 시행착오를 겪어왔나요? 응, 나도 그랬어요. 하지만 오늘은 CSS 가상 선택자에 자신감을 갖는 날이에요... 뭐든 의미가 뭐든 lol 😉
이 글에서는 두 가지 모두 예시를 들어 설명하고 각각 사용하는 몇 가지 스타일링 팁을 공유할 거에요.</p>
<p>차이점
콜론 :는 엘리먼트의 상태를 앞서고 식별하는 반면, 더블 콜론 ::은 엘리먼트(들)을 &quot;생성&quot;해요. :와 ::의 차이는 전자가 일반적으로 사용자 상호작용을 포함하는 선택된 엘리먼트의 상태를 설명하는 반면, 후자는 선택된 엘리먼트의 일부로 엘리먼트(들)을 만드는 데 사용되거나 선택된 엘리먼트를 참조로 사용해 엘리먼트들을 대상으로 하는 데 사용됩니다.</p>
<p>중요한 점은 :가 가상 클래스를 생성한다는 것이에요. 예를 들어
:hover - 사용자가 엘리먼트 위에 올라가면(선택하지 않은 상태에서) 엘리먼트를 스타일링하기 위해 사용합니다
:active - 엘리먼트를 클릭할 때(활성 상태일 때) 엘리먼트를 스타일링하기 위해 사용합니다
:visited - 사용자가 클릭한 앵커 태그(링크)를 스타일링하기 위해 사용합니다
:focus - 사용자가 클릭한 입력 필드를 스타일링하기 위해 사용합니다</p>
